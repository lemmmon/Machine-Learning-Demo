package toolsMadeByMyself;

public class Node {
	int key;	// class
	double val;	//value
	public Node(int key, double val) {
		this.key = key;
		this.val = val;
	}
	public int getKey() {
		return key;
	}
	public void setKey(int key) {
		this.key = key;
	}
	public double getVal() {
		return val;
	}
	public void setVal(double val) {
		this.val = val;
	}
}
